package sample;
public class pos {
    private double x;
    private double y;
    public pos(double x,double y)
    {
        x=this.x;
        y=this.y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
